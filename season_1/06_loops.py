# Season 1 - Loops

# while loop
i = 1
while i <= 5:
    print(i)
    i += 1

# for loop
for j in range(5):
    print(j)

# for loop with range
for k in range(1, 6):
    print(k)

# break example
for x in range(10):
    if x == 5:
        break
    print(x)

# continue example
for y in range(5):
    if y == 2:
        continue
    print(y)