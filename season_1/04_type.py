# season 1 - Type and Input

# get input from user
age = input("Enter your age : ")
print(age)
print(type(age))

# convert input to integer
age = int(age)
print(age)
print(type(age))

# float input
height = float(input("Enter your height in meters : "))
print(height)
print(type(height))

# boolean example
is_student = True
print(is_student)
print(type(is_student))